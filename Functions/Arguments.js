// Function arguments.

// We know how to call functions and get values from them (out of them),
// but what about getting values INTO them?

function sumTheseTwoYo(a, b){
    return (a+b);
}

// in the above function declaration, a and b are called FORMAL ARGUMENTS
// when the function is called, formal arguments receive values and become ACTUAL ARGUMENTS

// **REMEMBER - arguments ONLY exist in the function. 